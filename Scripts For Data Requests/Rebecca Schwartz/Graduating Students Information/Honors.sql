--(End 1)---------------------------------------------------------------------------------------------------------------
     ) AS X
JOIN (VALUES ('Summa Cum Laude', 1), ('Magna Cum Laude', 2), ('Cum Laude', 3))
    AS MY_ORDER(LABEL, N) ON X.HONORS = MY_ORDER.LABEL
GROUP BY HONORS, MY_ORDER.N
--(End 2)---------------------------------------------------------------------------------------------------------------
ORDER BY MY_ORDER.N