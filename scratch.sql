SELECT DISTINCT STC_PERSON_ID,
       SCS_COURSE_SECTION,
       STC_CRED,
       CS.SEC_MIN_CRED,
       CS.SEC_MAX_CRED,
       CS.SEC_TERM
FROM STUDENT_ACAD_CRED AS STC
LEFT JOIN STC_STATUSES AS STATUS ON STC.STUDENT_ACAD_CRED_ID = STATUS.STUDENT_ACAD_CRED_ID AND POS = 1
LEFT JOIN STUDENT_COURSE_SEC AS STU_SEC ON STC.STC_STUDENT_COURSE_SEC = STU_SEC.STUDENT_COURSE_SEC_ID
LEFT JOIN COURSE_SECTIONS AS CS ON STU_SEC.SCS_COURSE_SECTION = CS.COURSE_SECTIONS_ID
LEFT JOIN TERMS ON STC.STC_TERM = TERMS_ID
WHERE STATUS.STC_STATUS IN ('N', 'A')
AND COALESCE(STU_SEC.SCS_PASS_AUDIT, '') != 'A'
AND (
      TERMS_ID LIKE '%FA'
      OR TERMS_ID LIKE '%SP'
      OR TERMS_ID LIKE '%SU'
  )
AND TERM_START_DATE < DATEADD(YEAR, 1, '2024-07-01')
AND TERM_END_DATE >= '2024-07-01'
AND STC.STC_CRED > 0
AND STC_CRED = CS.SEC_MIN_CRED

SELECT *
FROM COURSE_SECTIONS

